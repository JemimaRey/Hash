    private void btnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioActionPerformed
        if ((btnModulo.isSelected() || btnCuadrado.isSelected() || btnTruncamiento.isSelected()) && (btnDDH.isSelected() || btnPruebaLineal.isSelected())) {

            flagMod = btnModulo.isSelected();
            flagDDH = btnDDH.isSelected();
            flagCuadrado = btnCuadrado.isSelected();
            flagTruncamiento = btnTruncamiento.isSelected();

            this.frameH = new FrameHash();
            this.frameH.setArray(flagMod, flagDDH, flagCuadrado, flagTruncamiento);
            this.frameH.setVisible(true);
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar una opci贸n de cada una");

        }
    }//GEN-LAST:event_btnInicioActionPerformed

